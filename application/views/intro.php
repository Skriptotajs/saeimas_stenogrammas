<h2>Par vietni</h2>
<p>
	Šī vietne ir izveidota, lai atvieglotu darbu žurnālistiem, politologiem un citiem, kas interesējas par Saeimas darbu un deputātu viedokļiem. Ir izveidots Saeimas stenogrammu (no 5. līdz 11. Saeimai ieskaitot) korpuss, kas ļauj meklēt gan vienlaicīgi visās Saeimas stenogrammās, gan arī konkrētās sēdēs, laika posmos un pat konkrētu deputātu runā.
</p>
<p>
	Lai darbu atvieglotu, katrai konkordancei ir norādīts to teikušais deputāts, viņa dzimšanas gads, frakcija, kurā deputāts ir bijis, kā arī sēdes laiks un tips. Noklikšķinot uz rezultāta, tiek parādīts plašāks konteksts.
</p>
<p>
	Stenogrammas iegūtas no <a href="http://www.saeima.lv/lv/par-saeimu/arhivs" target="_blank">Saeima.lv</a>
</p>
